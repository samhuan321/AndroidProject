package com.example.qlnhasach;

public class HoaDon {
    public int idHD;
    public String tenKH;
    public int tongTien;
    public String diaChi;
    public String ngayDH;
    public String ngayGH;
    public String tinhTrang;
    public String loaitt;

    public HoaDon(int idHD, String tenKH, int tongTien, String diaChi, String ngayDH, String ngayGH, String tinhTrang, String loaitt) {
        this.idHD = idHD;
        this.tenKH = tenKH;
        this.tongTien = tongTien;
        this.diaChi = diaChi;
        this.ngayDH = ngayDH;
        this.ngayGH = ngayGH;
        this.tinhTrang = tinhTrang;
        this.loaitt = loaitt;
    }
}
